const main = async () => {
    const [deployer] = await hre.ethers.getSigners();
    const accountBalance = await deployer.getBalance();
  
    console.log("Deploying contracts with account: ", deployer.address);
    console.log("Account balance: ", accountBalance.toString());
  
    const TTStakingFactory = await hre.ethers.getContractFactory("TTStaking");
    const TTStaking = await TTStakingFactory.deploy();
    await TTStaking.deployed();
  
    console.log("TTStaking address: ", TTStaking.address);
  };
  
  const runMain = async () => {
    try {
      await main();
      process.exit(0);
    } catch (error) {
      console.log(error);
      process.exit(1);
    }
  };
  
  runMain();